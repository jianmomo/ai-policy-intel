def normalize_lang(lang: str | None) -> str:
    return lang if lang in SUPPORTED_LANGS else "zh"


def translate(lang: str, key: str, **values: Any) -> str:
    current_lang = normalize_lang(lang)
    catalog = TRANSLATIONS[current_lang]
    template = catalog.get(key, TRANSLATIONS["en"].get(key, key))
    return template.format(**values) if values else template



def get_translator(lang: str):
    current_lang = normalize_lang(lang)

    def _inner(key: str, **values: Any) -> str:
        return translate(current_lang, key, **values)

    return _inner
